/**
 * What are the different data types?
 * Number - byte, short, Int, Long, Float, Double
 * Boolean
 * Characters
 * Arrays
 * Strings
 */
fun main(args: Array<String>) {
val num: Byte = 99 // Kotlin compilers are smart
    println("$num");

    // Double
    val numDouble= 1011.89
    println("$numDouble");


    // Float
    val numFloat= 101.89F
    println("$numFloat");

    // Boolean
    val boolFlag= false
    println("$boolFlag");

}